#include<bits/stdc++.h>
using namespace std;

//Given two decimal numbers, display the possible results that can be obtained by applying the operators +, -, *, /, %

int main(){
    int a;
    int b;
    cout << "Enter your number: " << endl;
    cin >> a >> b;

    cout << "a + b = " << a + b << endl;
    cout << "a - b = " << a - b << endl;
    cout << "a * b = " << a * b << endl;
    cout << "a / b = " << a / b << endl;
    cout << "a % b = " << a % b << endl;

    return 0;
}